#include<iostream>
using namespace std;

//打印数据的函数
void showValue(const int & val) 
{
	//val = 1000;------//错误，常量不可修改
	cout << val << endl;
}


int main() 
{
	//常量引用
	//使用场景:用来修饰形参防止误操作
	
	//加上const之后，编译器将代码改为 int temp = 10; const int &  ref = temp;
	//const int &ref = 10;//引用必须引用合法的内存空间
	//ref = 100;------//加上const之后变为只读，不可修改

	int a = 100;
	showValue(a);


	system("pause");

	return 0;
}