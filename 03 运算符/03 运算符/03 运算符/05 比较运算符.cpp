#include<iostream>
using namespace std;

int main5() 
{
	int a = 4;
	int b = 3;
	//比较运算符(1真0假)
	// ==
	cout << (a == b) << endl;
	// !=
	cout << (a != b) << endl;
	//<
	cout << (a < b) << endl;
	//>
	cout << (a > b) << endl;
	//<=
	cout << (a <= b) << endl;
	//>=
	cout << (a >= b) << endl;
	system("pause");

	return 0;
}